#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;
	char	*d;
	char	*s;

	d = (char *)dest;
	s = (char *)src;
	i = 0;
	if (!src && !dest && n > 0)
		return (NULL);
	while (n--)
		*d++ = *s++;
	return (dest);
}
