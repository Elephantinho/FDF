#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	long	ln;

	ln = n;
	if (ln < 0)
	{
		write(fd, "-", 1);
		ln *= -1;
	}
	if (ln > 9)
		ft_putnbr_fd(ln / 10, fd);
	c = ln % 10 + 48;
	write(fd, &c, 1);
}
